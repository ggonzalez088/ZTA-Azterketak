package bl;

public class GelaJB {

	private String gela;
	private double denbora;
	
	public GelaJB() {
	}
	
	public GelaJB(String gela, double denbora) {
		super();
		this.gela = gela;
		this.denbora = denbora;
	}

	public double getDenbora() {
		return denbora;
	}
	public void setDenbora(double denbora) {
		this.denbora = denbora;
	}
	public String getGela() {
		return gela;
	}
	public void setGela(String gela) {
		this.gela = gela;
	}
	
	
}
